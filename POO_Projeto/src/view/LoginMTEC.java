    public void logarEvents() {
        if (verificarCampos()) {
            Pessoa pessoaLogada = logar();
            if (pessoaLogada != null) {
                if (pessoaLogada.getId() == 1) {
                    new TelaAdmin(pessoaLogada, con);
                    this.dispose();
                } else {
                    new TelaPessoa(pessoaLogada, con);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Dados inv√°lidos, tente novamente");
            }
        }
    }

    private Pessoa logar() {
        String login = this.tflogin.getText();
        String senha = new String(this.jpassword.getPassword());
        String senhaCriptografada = new CriptoPessoa().criptografa(senha);
        Pessoa pessoa = new Pessoa();
        pessoa.setLogin(login);
        pessoa.setSenha(senhaCriptografada);
        Pessoa pessoaLogavel = pessoaDao.searchPessoaByLogin(login);
        if (pessoaLogavel == null) {
        } else {
            if (pessoaLogavel.getSenha().equals(senhaCriptografada)) {
                return pessoaLogavel;
            }
        }
        return null;
    }


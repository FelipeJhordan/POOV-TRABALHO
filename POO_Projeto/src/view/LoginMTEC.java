    public boolean verificarCampos() {
        String senha = new String(jpassword.getPassword());
        if (this.tflogin.getText().equals("") || senha.equals("")) {
            if (tflogin.getText().equals("") && senha.equals("")) {
                JOptionPane.showMessageDialog(this, "Campos em branco");
            } else if (tflogin.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Campo login em branco");
                this.tflogin.requestFocus();
            } else if (senha.equals("")) {
                JOptionPane.showMessageDialog(this, "Campo senha em branco");
                this.jpassword.requestFocus();
            }
            shake(this);
            return false;
        }
        return true;
    }
